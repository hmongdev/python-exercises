#/////////////////////////math///////////////////////////
#concepts: operators, augmented assignment operator, comparison, logical

print(10 + 3) #//13
print(10 - 3) #//7
print(10 * 3) #//30
print(10 ** 3) #//1_000, exponent
print(10 / 3) #//3.3333333333333333, decimal
print(10 // 3) #//3; this one just returns the integer, not the decimal part of it
print(10 % 3) #//1, modulus operator returns only the remainder

#augmented assignment operator
# x = 10
# (x = x + 3) is the same as (x += 3) #//13

